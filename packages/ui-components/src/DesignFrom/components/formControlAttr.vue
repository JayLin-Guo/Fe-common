  divider: 'ÂàÜÂâ≤Á∫ø',
  card: 'Âç°Áâá',
  tabs: 'Ê†áÁ≠æÈ°µ',
  flex: 'ÂºπÊÄßÂ≠óÊÆµ',
  div: 'ÂÆπÂô®',
};

// Ëé∑ÂèñÂÖÉÁ¥†Á±ªÂûãÊòæÁ§∫ÂêçÁß∞
const getElementTypeName = type => {
  return elementTypeNames[type] || type;
};

// ÁõëÂê¨ÈÄâ‰∏≠ÂÖÉÁ¥†ÁöÑÂèòÂåñ
watch(
  () => props.selectedElement,
  newElement => {
    console.log('üéØ Â±ûÊÄßÈù¢ÊùøÊé•Êî∂Âà∞Êñ∞ÈÄâ‰∏≠ÂÖÉÁ¥†:', newElement);

    if (newElement) {
      localElement.value = JSON.parse(JSON.stringify(newElement));
      console.log('üìã ÂàõÂª∫Êú¨Âú∞ÂâØÊú¨:', localElement.value);

      // ÂàùÂßãÂåñÈªòËÆ§Â±ûÊÄßÂÄº
      try {
        initElementAttrs(
          localElement.value,
          newElement.type,
          props.customAttrConfig
        );
      } catch (error) {
        console.error('ÂàùÂßãÂåñÂ±ûÊÄßÂ§±Ë¥•:', error);
      }

      // Á°Æ‰øùÂøÖË¶ÅÁöÑÂ±ûÊÄßÂ≠òÂú®
