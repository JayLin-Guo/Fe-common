};

// Ê∑ªÂä†ÈÄâÈ°π
const addOption = path => {
  const options = getNestedValue(localElement.value, path) || [];
  options.push({
    label: 'Êñ∞ÈÄâÈ°π',
    value: 'new_option_' + Date.now(),
  });
  setNestedValue(localElement.value, path, options);
  updateElement();
};

// Âà†Èô§ÈÄâÈ°π
const removeOption = (path, index) => {
  const options = getNestedValue(localElement.value, path) || [];
  options.splice(index, 1);
  setNestedValue(localElement.value, path, options);
  updateElement();
};
</script>

