import {
  getAttrConfig,
  getNestedValue,
  setNestedValue,
  initElementAttrs,
} from './attrConfig.js';

// È™åËØÅÂØºÂÖ•
if (!getAttrConfig) {
  console.error('getAttrConfig ÂØºÂÖ•Â§±Ë¥•');
}

const props = defineProps({
  selectedElement: {
    type: Object,
    default: null,
  },
  customAttrConfig: {
    type: Object,
